//This is a function that takes string as a parameter, then returns a reversed version of the string.

function reverse(str){
	return str.split('').reverse().join('')
}


/*This code uses three methods : .split() , .reverse(), .join()
 * the .split() method converts the string to an array
 * the .reverse() method reverses the array
 * the .join() method changes the array back to a string
 */
